# working with if else condition in Ruby
number = -5

if number > 0
  puts "#{number} is a positive number."
else
  puts "#{number} is not a positive number."
end

if number.even?
  puts "#{number} is an even number."
else
  puts "#{number} is not an even number."
end

if number < 0
  puts "#{number} is a negative number."
else
  puts "#{number} is not a negative number."
end