# array in ruby
numbers = [1, 2, 3, 4, 5]
puts numbers # print the array


fruits = ["apple", "banana", "cherry"]
puts fruits # print the array of fruits


# mixed data types
mixed_array = [1, "two", 3.0, true, nil]
puts mixed_array # print the mixed array


# empty array
empty_array = []
empty_array2 = Array.new
puts empty_array # print the empty array


# creating array using Array literal
array_literal = Array[10, 20, 30]
puts array_literal


# array create using Array.new
array1 = Array.new(3)          # array of size 3 with nil values
puts array1


# creating array with default values
array2 = Array.new(4, "hello") # array of size 4 with all elements as "hello"
puts array2





