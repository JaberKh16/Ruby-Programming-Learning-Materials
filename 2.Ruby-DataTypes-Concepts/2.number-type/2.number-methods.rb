# define a number
number = 42


# methods to manipulate numbers
puts number.abs            # absolute value
puts number.round          # round to nearest integer
puts number.ceil           # round up to nearest integer
puts number.floor          # round down to nearest integer
puts number.to_s           # convert to string
puts number.even?         # check if the number is even
puts number.odd?          # check if the number is odd
puts number.next          # get the next integer
puts number.pred          # get the previous integer
puts number.to_f          # convert to float
puts number.zero?         # check if the number is zero
puts number.nonzero?      # return number if non-zero, else nil
puts number.div(5)       # integer division by 5
puts number.modulo(7)    # modulus operation with 7
puts number.pow(2)       # raise to the power of 2
puts number.sqrt          # square root of the number